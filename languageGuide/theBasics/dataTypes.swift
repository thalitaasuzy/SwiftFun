//Tipos de Coleções
Array: Representa uma coleção ordenada de elementos do mesmo tipo.
Set: Representa uma coleção não ordenada de elementos únicos.
Dictionary: Representa uma coleção de pares chave-valor.

//Tipos Opcionais:
Optional: Permite representar valores que podem estar ausentes (nil).

//Tipos de Função:
() -> Void: Representa uma função que não possui argumentos e não retorna um valor.

//Tipos de Estruturas:
Struct: Permite a criação de tipos de dados personalizados, como uma estrutura.

//Tipos de Classes:
Class: Permite a criação de tipos de dados personalizados, que suportam herança.

//Tipos de Enumerações:
Enum: Permite a criação de tipos de dados que podem ter um de vários casos (valores).

//Tipos de Tuplas:
Tuple: Permite agrupar valores de tipos diferentes.

//Tipos de Closures:
Closure: Representa um bloco de código que pode ser armazenado e passado como um valor.

//Tipos de Protocolos:
Protocol: Define um conjunto de requisitos que as classes ou estruturas podem adotar.

//Tipos de Índices de Coleções:
Index: Usado para acessar elementos em uma coleção.
